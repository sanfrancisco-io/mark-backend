# ── Helpers ────────────────────────────────────────────────────────────────────

def _random_price(base: float) -> float:
    return round(random.uniform(base * 0.8, base * 1.2), 2)


def _base_price_for_category(category_name: str) -> float:
    ranges = {
        "Электроника": (1_500, 80_000),
        "Одежда":      (500,   15_000),
        "Спорт":       (300,   25_000),
        "Дом и быт":   (400,   20_000),
    }
    lo, hi = ranges.get(category_name, (500, 10_000))
    return round(random.uniform(lo, hi), 2)


async def _upload_placeholder(object_key: str, color: tuple) -> tuple[str, str]:
    img_bytes = make_placeholder_png(400, 400, color)
    thumb_bytes = make_placeholder_png(100, 100, color)
    await upload_file(object_key, img_bytes, "image/png")
    await upload_file(f"thumb_{object_key}", thumb_bytes, "image/png")
    return object_key, f"thumb_{object_key}"


# ── Main ───────────────────────────────────────────────────────────────────────

async def main() -> None:
    print("→ Ensuring MinIO bucket exists...")
    await ensure_bucket_exists()

    async with AsyncSessionLocal() as session:
        # ── Check if already seeded ──
        result = await session.execute(text("SELECT COUNT(*) FROM sellers"))
        if result.scalar() > 0:
            print("✓ DB already seeded, skipping.")
            return

        # ── Sellers ──
        print("→ Creating sellers...")
        sellers: list[Seller] = []
        for name, rating in SELLERS:
            seller = Seller(name=name, rating=rating)
            session.add(seller)
            sellers.append(seller)
        await session.flush()

        # ── Products, attributes, offers ──
        product_count = 0
        for category in CATEGORIES:
            cat_name: str = category["name"]
            cat_color: tuple = category["color"]
            attr_pool: dict = category["attributes"]

            print(f"→ Creating {cat_name} ({len(category['products'])} products)...")

            for prod_name, prod_desc in category["products"]:
                base_price = _base_price_for_category(cat_name)
                object_key = f"product_{product_count:04d}.png"

                img_key, thumb_key = await _upload_placeholder(object_key, cat_color)

                product = Product(
                    name=f"{prod_name}",
                    description=prod_desc,
                    price_amount=base_price,
                    price_currency="RUB",
                    stock=random.randint(0, 200),
                    image_object_key=img_key,
                    thumbnail_object_key=thumb_key,
                )
                session.add(product)
                await session.flush()

                # Attributes: pick 2–6 random attr keys from pool
                attr_keys = random.sample(list(attr_pool.keys()), k=min(random.randint(2, 6), len(attr_pool)))
                for attr_name in attr_keys:
                    session.add(ProductAttribute(
                        product_id=product.id,
                        name=attr_name,
                        value=random.choice(attr_pool[attr_name]),
                    ))

                # Offers: 2–10 random sellers
                chosen_sellers = random.sample(sellers, k=random.randint(2, min(10, len(sellers))))
                for seller in chosen_sellers:
                    session.add(Offer(
                        product_id=product.id,
                        seller_id=seller.id,
                        price_amount=_random_price(base_price),
                        price_currency="RUB",
                        delivery_date=date.today() + timedelta(days=random.randint(0, 6)),
                    ))

                product_count += 1

        await session.commit()

    print(f"✓ Seeded: {len(SELLERS)} sellers, {product_count} products across {len(CATEGORIES)} categories.")


if __name__ == "__main__":
    asyncio.run(main())
